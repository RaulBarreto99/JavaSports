create table "ROOT".CLIENTE
(
	id INT not null primary key
        GENERATED ALWAYS AS IDENTITY
        (START WITH 1, INCREMENT BY 1),
	NOME VARCHAR(255),
	SOBRENOME VARCHAR(255),
	DATANASCIMENTO DATE,
	CPF VARCHAR(12),
	TELEFONE VARCHAR(14),
	SEXO VARCHAR(20)
);


create table "ROOT".PRODUTO(
	IDPRODUTO BIGINT not null primary key
        GENERATED ALWAYS AS IDENTITY
        (START WITH 1, INCREMENT BY 1),
    NOME VARCHAR(60),
    MARCA VARCHAR(60),
    PRECO DECIMAL(10,2),
    QUANTIDADE INT
);

create table "ROOT".FILIAL(
	IDPRODUTO BIGINT not null primary key
        GENERATED ALWAYS AS IDENTITY
        (START WITH 1, INCREMENT BY 1),
    NOME VARCHAR(60),
    Bairro VARCHAR(60),
    CEP VARCHAR(60),
    PAIS VARCHAR(60),
    RUA VARCHAR(60),
    COMPLEMENTO VARCHAR(60),
    NUMERO VARCHAR(60),
    UF VARCHAR(60),
    CIDADE VARCHAR(60)
);
